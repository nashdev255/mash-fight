.include "tn2313def.inc"
.CSEG

SETUP:
	LDI R16, 0b11111111
	OUT DDRB, R16
	LDI R16, 0b11111100
	OUT DDRD, R16
	RCALL RESET

JUDGE:
	IN R16, PIND
	RCALL T100US
	ANDI R16, 0b00000001
	BREQ HIT
	CPI R16, 0b00000010
	BREQ RESET
	RCALL DISPLAY
	RJMP JUDGE

HIT:
	INC R18
	RET

DISPLAY:
	RCALL CALLYJUDGE
	OUT PORTB, R18
	RET

CARRYJUDGE:
	CPI R18, 0b00011010
	BREQ CARRYUP
	CPI R18, 0b00111001
	BREQ CARRYDOWN
	RJMP JUDGE ; PINDの状態確認ラベル
	RET

CARRYUP:
	ORI R18, 0b00110000
	RJMP CARRYJUDGE

CARRYDOWN:
	ANDI R18, 0b00011111
	RJMP CARRYJUDGE

RESET:
	LDI R16, 0b00010000
	OUT PORTB, R16
	RET

; 20031004[Hz]
T1S:
    LDI R23, 100
    _T10MS:
        RCALL T10MS
        SUBI R23, 1
        BRNE _T10MS
        RET

; 200304[Hz]
T10MS:
    LDI R22, 100
    _T100US:
        RCALL T100US
        SUBI R22, 1
        BRNE _T100US
        RET

; 1997[Hz]
T100US:
    LDI R21, 249
    _TUS:
        NOP
        NOP
        NOP
        NOP
        NOP
        SUBI R21, 1
        BRNE _TUS
        NOP
        RET